/*Faça um programa que imprima em ordem decrescente todos os números entre 40 e 20. Utilize laço while e outras estruturas que julgar necessárias.*/

public class Main {
    public static void main(String[] args) {
        int num = 40;
        while(num >=20){
            System.out.println(num);
            num--;
        }
    }
}
